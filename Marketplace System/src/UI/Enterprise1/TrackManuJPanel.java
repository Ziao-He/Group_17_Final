    /**
     * Load all orders for current buyer and display in table
     */
    private void loadOrders() {
        DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
        model.setRowCount(0);
        
        if (buyerAccount == null || system == null) {
            return;
        }
        
        // Get buyer's order IDs
        List<String> orderIds = buyerAccount.getOrderIds();
        if (orderIds == null || orderIds.isEmpty()) {
            return;
        }
        
        // Date formatter for better display
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");
        
        // Load each order
        for (String orderId : orderIds) {
            // Find order in system
            Order order = system.getOrderDirectory().findById(orderId);
            if (order == null) {
                continue;
            }
            
            // Get product title from listing
            String productTitle = order.getListingId(); // fallback to ID
            if (system.getListingDirectory() != null) {
                Listing listing = system.getListingDirectory().findById(order.getListingId());
                if (listing != null) {
                    productTitle = listing.getTitle();
                }
            }
            
            // Get seller username
            String sellerName = order.getSellerId(); // fallback to ID
            UserAccount seller = system.getUserAccountDirectory().findByUserId(order.getSellerId());
            if (seller != null) {
                sellerName = seller.getUsername();
            }
            
            // Format dates
            String createdDate = "N/A";
            if (order.getCreatedAt() != null) {
                createdDate = order.getCreatedAt().format(formatter);
            }
            
            String updatedDate = "N/A";
            if (order.getUpdatedAt() != null) {
                updatedDate = order.getUpdatedAt().format(formatter);
            }
            
            // Create row (7 columns)
            Object[] row = {
                order.getOrderId(),                           // Order ID
                productTitle,                                 // Product
                sellerName,                                   // Seller
                String.format("$%.2f", order.getTotalPrice()), // Price
                order.getStatus(),                            // Status
                createdDate,                                  // Created Date
                updatedDate                                   // Updated Date
            };
            
            model.addRow(row);
        }
        
        System.out.println("Loaded " + model.getRowCount() + " orders for buyer: " + buyerAccount.getUsername());
    }
