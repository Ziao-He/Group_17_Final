    private void loadTable() {
          DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
    model.setRowCount(0);

boolean isSystemAdmin =
        admin.getRole() instanceof basement_class.Enterprise_3.Role.SystemAdminRole;

   
    for (Network n : system.getNetworks()) {
        for (Enterprise e : n.getEnterprises()) {
            for (Organization org : e.getOrganizations()) {
                for (WorkRequest req : org.getWorkRequestDirectory().getRequestList()) {

                  
                  if (isSystemAdmin) {
    if (req.isResolved()) {
        addRow(req, model);   
    }
} else {
    if (req.isResolved()
            && req.getReviewer() != null
            && req.getReviewer().equals(admin)) {

        addRow(req, model);  
    }
}
                }
            }
        }
    }
    }  

    private void addRow(WorkRequest req, DefaultTableModel model) {

    Object[] row = new Object[5];

    row[0] = req.getId();  // âœ… Request ID

    row[1] = (req.getReviewer() == null)
            ? "N/A"
            : req.getReviewer().getUsername();  

    row[2] = req.getReviewAction();            

    row[3] = getRequestType(req);              

    row[4] = req;                             

    model.addRow(row);
}

private String getRequestType(WorkRequest req) {

    if (req instanceof basement_class.Enterprise_3.WorkRequest.RegistrationApprovalRequest)
        return "Registration";

    if (req instanceof basement_class.Enterprise_3.WorkRequest.AccountStatusReviewRequest)
        return "Account Status";

    if (req instanceof basement_class.Enterprise_3.WorkRequest.ListingReviewRequest)
        return "Listing Review";

    if (req instanceof basement_class.Enterprise_3.WorkRequest.PolicyViolationRequest)
        return "Policy Violation";

    return "Unknown";
}
