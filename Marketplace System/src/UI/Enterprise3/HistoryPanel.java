    private void btnDescpritonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescpritonActionPerformed
         int row = tblUser.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Please select a record first.");
        return;
    }

    WorkRequest req = (WorkRequest) tblUser.getValueAt(row, 4);

    String comment = req.getReviewComment();
    if (comment == null || comment.isBlank()) {
        comment = "(No comment)";
    }

    String msg = """
        Request ID: %s
        Reviewer: %s
        Type: %s
        Action: %s
        Time: %s

        Comment:
        %s
        """.formatted(
            req.getId(),
            req.getReviewer() == null ? "N/A" : req.getReviewer().getUsername(),
            getRequestType(req),
            req.getReviewAction(),
            req.getResolveDate(),
            comment
    );

    JOptionPane.showMessageDialog(this, msg, "History Detail", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnDescpritonActionPerformed

