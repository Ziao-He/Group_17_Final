    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        int row = tblRequests.getSelectedRow();
        if (row < 0 || row >= requestList.size()) {
            JOptionPane.showMessageDialog(this, "Please select a request.");
            return;
        }

        MessageFlagRequest req = requestList.get(row);

        // Only pending can be processed
        if (req.getStatus() != null && !req.getStatus().equalsIgnoreCase("Pending")) {
            JOptionPane.showMessageDialog(this, "This request is already processed.");
            return;
        }

        //marked message
        Message msg = req.getFlaggedMessage();
        if (msg != null) {
            msg.contentIsInappropriate();
        }

        // update request status
        req.setStatus("Approved");

        JOptionPane.showMessageDialog(this, "Request approved.");

        reloadRequests();

    
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:
        int row = tblRequests.getSelectedRow();
        if (row < 0 || row >= requestList.size()) {
            JOptionPane.showMessageDialog(this, "Please select a request.");
            return;
        }

        MessageFlagRequest req = requestList.get(row);

        if (req.getStatus() != null && !req.getStatus().equalsIgnoreCase("Pending")) {
            JOptionPane.showMessageDialog(this, "This request is already processed.");
            return;
        }

        req.setStatus("Rejected");

        JOptionPane.showMessageDialog(this, "Request rejected.");

        reloadRequests();
    }//GEN-LAST:event_btnRejectActionPerformed

