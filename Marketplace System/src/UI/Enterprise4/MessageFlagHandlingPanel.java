    private void reloadRequests() {
        requestList.clear();
        DefaultTableModel tableModel = (DefaultTableModel) tblRequests.getModel();
        tableModel.setRowCount(0);

        if (communicationServiceOrg == null
                || communicationServiceOrg.getWorkRequestDirectory() == null) {
            return;
        }

        for (WorkRequest wr : communicationServiceOrg.getWorkRequestDirectory().getRequestList()) {
            if (wr instanceof MessageFlagRequest) {
                MessageFlagRequest mfr = (MessageFlagRequest) wr;
                requestList.add(mfr);

                String id = mfr.getId();
                UserAccount reporter = mfr.getSender();
                String reporterName = (reporter != null) ? reporter.getUsername() : "Unknown";

                Message msg = mfr.getFlaggedMessage();
                
                String msgPreview = msg.getContent();

                String reason = mfr.getReason();
                
                String status = mfr.getStatus();

                tableModel.addRow(new Object[]{id, reporterName, msgPreview, reason, status});
            }
        }

        tfMessageDetail.setText("");
        tfReasonDetail.setText("");
        btnApprove.setEnabled(false);
        btnReject.setEnabled(false);
    }
    
    private void onTableSelectionChanged() {
        int row = tblRequests.getSelectedRow();
        if (row < 0 || row >= requestList.size()) {
            tfMessageDetail.setText("");
            tfReasonDetail.setText("");
            btnApprove.setEnabled(false);
            btnReject.setEnabled(false);
            return;
        }

        MessageFlagRequest req = requestList.get(row);

        // show detail on textfiled
        Message msg = req.getFlaggedMessage();
        tfMessageDetail.setText(msg != null ? msg.getContent() : "");
        tfReasonDetail.setText(req.getReason() != null ? req.getReason() : "");

        // Determine whether the button is available based on its status
        String status = req.getStatus();
        if (status.equalsIgnoreCase("Pending")) {
            btnApprove.setEnabled(true);
            btnReject.setEnabled(true);
        } else {
            btnApprove.setEnabled(false);
            btnReject.setEnabled(false);
        }
    }


