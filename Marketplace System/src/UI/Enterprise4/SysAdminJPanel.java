    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblUserAccounts.getModel();
        model.setRowCount(0);

        UserAccountDirectory uaDir = system.getUserAccountDirectory(); 
        if(uaDir.size()<=0)
            return;

        for (UserAccount ua : uaDir.getUserAccounts()) {
            Object[] row = new Object[4];
            row[0] = ua;                           
            row[1] = ua.getUsername();             
            row[2] = ua.getPasswordHash();
            row[3] = ua.getRole().getRoleName();
            model.addRow(row);
        }
    }
