    private void reloadConversation() {
        taConversation.setText("");

        UserAccount partner = (UserAccount) cbChatPartner.getSelectedItem();
        if (partner == null) return;

        ArrayList<Message> list = messageDirectory.getMessagesBetween(currentUser, partner);
        for (Message m : list) {
            appendMessageToConversation(m);
        }
    }
   
   
    private void reloadReportCombo() {
        cbMessageToReport.removeAllItems();

        UserAccount partner = (UserAccount) cbChatPartner.getSelectedItem();
        if (partner == null) return;

        ArrayList<Message> list = messageDirectory.getMessagesBetween(currentUser, partner);
        for (Message m : list) {
            // Only allow the current user to report the message "sent by the other party"
            if (!m.getSender().equals(currentUser)) {
                cbMessageToReport.addItem(m);
            }
        }
    }
    private void appendMessageToConversation(Message m) {
        if (m == null) return;
        String senderName = (m.getSender() != null) ? m.getSender().getUsername() : "Unknown";
        taConversation.append(senderName + ": " + m.getContent() + "\n");
    }

