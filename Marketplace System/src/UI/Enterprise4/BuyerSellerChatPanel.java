    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        UserAccount partner = (UserAccount) cbChatPartner.getSelectedItem();
        if (partner == null) {
            JOptionPane.showMessageDialog(this, "Please select a chat partner.");
            return;
             }

        String text = tfMessageInput.getText().trim();
        if (text.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Message cannot be empty.");
            return;
        }

        Message msg = messageDirectory.addMessage(currentUser, partner, text);

        // update message to chat area
        if (msg != null) {
            appendMessageToConversation(msg);
        }

        tfMessageInput.setText("");
        reloadReportCombo(); 
    }//GEN-LAST:event_btnSendActionPerformed

    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        // TODO add your handling code here:
        Message selectedMessage = (Message) cbMessageToReport.getSelectedItem();
        if (selectedMessage == null) {
            JOptionPane.showMessageDialog(this, "Please select a message to report.");
            return;
        }

        String reason = tfReportReason.getText().trim();
        if (reason.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a reason for the report.");
            return;
        }

        
        selectedMessage.flagByUser();

        MessageFlagRequest req = new MessageFlagRequest(selectedMessage, currentUser, reason);
        // someOrganization.getWorkRequestDirectory().addRequest(req);

        JOptionPane.showMessageDialog(this, "Report submitted. Thank you.");

        tfReportReason.setText("");
    }//GEN-LAST:event_btnReportActionPerformed

