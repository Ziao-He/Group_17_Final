    /**
    * Find the organization that the user belongs to
    * @param user The user account
    * @param system The ecosystem instance
    * @return Organization or null if not found
    */
   private Organization findUserOrganization(UserAccount user, EcoSystem system) {
       for (Network network : system.getNetworks()) {
           for (Enterprise enterprise : network.getEnterprises()) {
               for (Organization org : enterprise.getOrganizations()) {
                   UserAccount found = org.getUserAccountDirectory()
                       .findByUserId(user.getUserId());
                   if (found != null) {
                       return org;
                   }
               }
           }
       }
       return null;
   }

   /**
    * Find the enterprise that contains the given organization
    * @param organization The organization
    * @param system The ecosystem instance
    * @return Enterprise or null if not found
    */
   private Enterprise findUserEnterprise(Organization organization, EcoSystem system) {
       if (organization == null) {
           return null;
       }

       for (Network network : system.getNetworks()) {
           for (Enterprise enterprise : network.getEnterprises()) {
               if (enterprise.getOrganizations().contains(organization)) {
                   return enterprise;
               }
           }
       }
       return null;
   }

 
    
